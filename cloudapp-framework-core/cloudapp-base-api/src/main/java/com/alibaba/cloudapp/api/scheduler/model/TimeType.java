package com.alibaba.cloudapp.api.scheduler.model;

public enum TimeType {
    NONE(-1, "none"),
    /**
     * Specifically refers to the quartz cron expression
     */
    CRON(1, "cron"),
    /**
     * The time interval of fixed speed, in seconds
     */
    FIXED_DELAY(2, "fixed_delay"),
    /**
     * The time interval of fixed speed, in seconds
     */
    FIXED_RATE(3, "fixed_rate");

    private int value;
    private String description;

    TimeType(int value, String description) {
        this.value = value;
        this.description = description;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
